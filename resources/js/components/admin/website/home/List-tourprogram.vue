<!--
This Template is for listing for the encyclopedia profile using function to get the 
data from the api to display the data about the encyclopedia from the backend .
-->
<template>
  <section class="content">
    <!--************************************************
      Template Type: encyclopedias List
      Author:@Ajay
    ****************************************************-->
    <div class="row justify-content-around">
      <div class="col-md-12">
        <div class="container container_admin_body">
          <div class="top_btn">
            <router-link :to="`/tourprogram-add`">Add tourprogram</router-link>
          </div>
          <!-- Start Card -->
          <table
            id="example"
            class="display table table-striped table-bordered nowrap"
            style="width:100%"
          >
            <thead>
              <tr>
                <th>
                  TITLE
                  <i class="fas fa-sort"></i>
                </th>
                <th>
                  IMAGE
                  <i class="fas fa-sort"></i>
                </th>
                <th>
                  DESCRIPTION
                  <i class="fas fa-sort"></i>
                </th>
                <th>
                  <i class="fas fa-cog"></i>
                </th>
              </tr>
            </thead>
            <tbody>
              <tr
                v-for="tourprogram in tourprogram_list"
                role="row"
                v-bind:class="{ odd: oddclass , 'even': evenclass}"
                class="row_list"
              >
                <td>{{ tourprogram.title }}</td>
                <td>
                  <img :src="getImgUrl(tourprogram.image)" />
                </td>
                <td>
                  <span v-html="tourprogram.description"></span>
                </td>
                <td class="edit_section">
                  <router-link :to="`tourprogram/${tourprogram.id}`" class="edit_link">
                    <span class="badge badge-primary">
                      <i class="fas fa-pencil-alt"></i>
                    </span>
                  </router-link>
                  <!-- <a href="" class="delete_link" @click.prevent = "deletetourprogram(tourprogram.id)" ><span class="badge badge-danger"><i class="far fa-trash-alt"></i></span></a> -->
                </td>
              </tr>
            </tbody>
          </table>
        </div>
        <!-- end -->
      </div>
    </div>
  </section>
  <!-- /.content -->
</template>

<script>
export default {
  name: "List",
  data() {
    return {
      oddclass: false,
      evenclass: true,
      tourprogram_list: []
    };
  },
  created() {
    this.encyclopediaData();
  },

  methods: {
    encyclopediaData() {
      axios.get("/api/tourprogram").then(response => {
        setTimeout(() => $("#example").DataTable(), 1000);
        this.tourprogram_list = response.data;
      });
    },
    getImgUrl(img) {
      return "/images/tourprogram/" + img;
    },

    deletetourprogram(id) {
      var uri = "/api/tourprogram/" + id;
      this.$swal
        .fire({
          title: "Are you sure?",
          text: "You won't be able to revert this!",
          type: "warning",
          showCancelButton: true,
          confirmButtonColor: "#3085d6",
          cancelButtonColor: "#d33",
          confirmButtonText: "Yes, delete it!"
        })
        .then(result => {
          if (result.value) {
            axios.delete(uri).then(response => {
              console.log(response);
              this.encyclopediaData();
            });
            this.$swal.fire(
              "Deleted!",
              "Your file has been deleted.",
              "success"
            );
          }
        });
    }
  }
};
</script>

