<!--
This Template is for listing for the school trips.
-->
<template>
  <list-layout addurl="/school-trip/add" buttontext="add trip">
    <template #perpage>
      <b-form-group
        label="Per page"
        label-for="per-page-select"
        label-cols-sm="6"
        label-cols-md="4"
        label-cols-lg="3"
        label-align-sm="right"
        label-size="sm"
        class="mb-0"
      >
        <b-form-select
          id="per-page-select"
          class="radius-0"
          v-model="perPage"
          :options="options"
        ></b-form-select>
      </b-form-group>
    </template>
    <template #searchbar>
      <b-form-input v-model="filter" type="search" placeholder="Type to Search" class="radius-0"></b-form-input>
    </template>
    <template #table>
      <b-table
        id="table-transition"
        striped
        hover
        sticky-header="460px"
        class="w-100 table-layout"
        :fields="fields"
        :items="items.data"
        :busy="$store.getters.isBusy"
        :filter="filter"
        primary-key="updated_at"
      >
        <template #table-busy>
          <table-loader />
        </template>
       <template #cell(booking_status)="data">
          <span 
              v-if="data.item.booking_status == 1" 
              class="badge badge-success">Confirmed</span>
          <span class="badge badge-warning" v-else>In progress</span>
        </template>
         <template #cell(payment_status)="data">
          <span 
              v-if="data.item.payment_status == 1" 
              class="badge badge-success">Complete
          </span>
          <span class="badge badge-info" v-else-if="data.item.payment_status == 3">In progress</span>
          <span class="badge badge-warning" v-else>Pending</span>
        </template>
        <template #cell(action)="data">
          <!-- <view-icon :url="`/view/${data.item.id}`"></view-icon> -->
          <edit-icon :url="`/school-trip/edit/${data.item.id}`"></edit-icon>
          <copy-icon :url="`/tour-page/${data.item.slug1}/${data.item.slug2}`"></copy-icon>
          
          <delete-icon 
            @click.native="deleteItem(data.item.id,data.index)"
            >
          </delete-icon>
        </template>
      </b-table> 
    </template>
    <template #pagination  v-if="items.data">
      <div class="w-100">
        <pagination :data="items" @pagination-change-page="getitems" :align="`right`" :limit="limit">
          <span slot="prev-nav">Previous</span>
          <span slot="next-nav">Next</span>
        </pagination>
      </div>  
    </template>
  </list-layout>
</template>

<script>
import listLayout from '@/admin/components/layout/ListLayout.vue';
import pagination  from 'laravel-vue-pagination';
import EditIcon from '@/admin/components/icons/EditIcon.vue';
import DeleteIcon from '@/admin/components/icons/DeleteIcon.vue';
import ViewIcon from '@/admin/components/icons/ViewIcon.vue';
import CopyIcon from '@/admin/components/icons/CopyIcon.vue';
import TableLoader from '@/admin/components/TableLoader.vue';
import { mapState } from 'vuex';

export default {
  name: "List",
  components:{
    'list-layout':listLayout,
    'table-loader':TableLoader,
    'pagination':pagination,
    'edit-icon':EditIcon,
    'delete-icon':DeleteIcon,
    'view-icon':ViewIcon,
    'copy-icon':CopyIcon,
  },
  data() {
    return {
      fields: [
        {key:'school_name',label:'school',sortable:true,thClass: 'table-head'},
        {key:'trip_name',label:'trip',sortable:true,thClass: 'table-head'},
        {key:'booking_status',label:'booking',sortable:true,thClass: 'table-head'},
        {key:'payment_status',label:'payment',thClass: 'table-head'},
        {key:'start_date',label:'start',sortable:true,thClass: 'table-head'},
        {key:'end_date',label:'end',sortable:true,thClass: 'table-head'},
        {key:'action',label:'action',thClass: 'table-head'}
      ],
      limit:2,
      filter:'',
      perPage:7,
      options:[7,25,50,100],
    };
  },
  mounted() {
    this.getitems();
  },
  computed:{
    ...mapState(['items']),
  },
  watch:{
    perPage:function(){
      this.getitems(1,this.perPage);
    }
  },

  methods: {
    getitems(page=1,size= this.perPage) {
      this.$store.dispatch('getItems','/schooltrip/all/'+size+'?page='+page);
    },
    deleteItem(id,index=-1) {
      let payload = {'api':"/schooltrip/"+id,index,'index':index};
      this.$store.dispatch('deleteItem',payload);
    },
  },
};
</script>


